function ocultar()
{
    document.getElementById("ventanaUsuario").style.display="none";
    document.getElementById("ventanaNuevoUsuario").style.display="block";
    document.getElementById("cp").style.display="none";
    document.getElementById("vnu").style.display="block";
}

function mostrar()
{
    document.getElementById("ventanaUsuario").style.display="block";
    document.getElementById("ventanaNuevoUsuario").style.display="none";
    document.getElementById("vnu").style.display="none";
    document.getElementById("cp").style.display="block";
}





